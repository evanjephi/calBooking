/**
 * Geocode a Canadian postal code to [lng, lat].
 * Uses first 3 characters (FSA) for lookup.
 *
 * @param {string} postalCode  e.g. "M5H 2N2", "m5h2n2", "L5B"
 * @returns {number[]|null}    [lng, lat] or null if FSA not found
 */
function geocodePostalCode(postalCode) {
  if (!postalCode || typeof postalCode !== "string") return null;
  const fsa = postalCode.replace(/\s+/g, "").toUpperCase().slice(0, 3);
  if (fsa.length < 3) return null;
  return FSA_COORDINATES[fsa] || null;
}

